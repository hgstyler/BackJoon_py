# Question 9498, BJ Online Judge
# https://www.acmicpc.net/problem/9498

score = int(input())

if score <= 100 and score >= 90:
    print('A')
elif score <= 89 and score >= 80:
    print('B')
elif score <= 79 and score >= 70:
    print('C')
elif score <= 69 and score >= 60:
    print('D')
else:
    print('F')

